//Execution context and Call Stack
//Execution context is the environment in which the code is executed
//Call Stack is a data structure that keeps track of function calls in JavaScript

//how the code is executed in JavaScript
//JavaScript is a single-threaded language, which means it can only execute one piece of code at a time.
//When a function is called, it is added to the call stack.
//When the function execution is completed, it is removed from the call stack.  


//Globlal execution context
//When the JavaScript engine starts executing the code, it creates a global execution context.
//This is the default context in which the code is executed.
//Function execution context
//When a function is called, a new execution context is created for that function.
//This context contains information about the function, such as its parameters, local variables, and the value of `this`.
//When the function execution is completed, the context is removed from the call stack, and the control is returned to the previous context.


